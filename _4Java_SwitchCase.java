package com.java.PW;

public class _4Java_SwitchCase {
public static void main(String[] args) {
	
	int num= 50;
	switch (num) {
	case 10 : System.out.println("Candy");
	break ;
	case 20 : System.out.println("Chocolate");
	break ;
	case 30 : System.out.println("Ice Cream");
	break ;
	case 40 : System.out.println("Juice");
	break ;
	case 50 : System.out.println("Juice");
	break ;
	default : System.out.println("Invalid");
	}
	
}
}
